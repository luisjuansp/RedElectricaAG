% GFUNS/
%   This directory contains integer version of functions operating
%   over generalized integer chromosomes.
%
% 
% GMUTATE
%    Returns the imutation of r at value k. The number r 
%    takes a minimal value of rMin.
% 
% GCROSSAT
%    Performs crossover of p1 and p2 at value m. 
% 
% GDIGIT
%    Digit m of base B of integer p. Digits are numbered
%    from least significant to most significant starting
%    at 1.
% 
% GHIGH
%    High part of integer p above digit m of base B.
% 
% GHIGHH
%    Digits of the high part of integer p above digit m of base B.
% 
% GLOW
%    Low part of integer p up to digit m of base B.
% 
% GMUTVALUES
%    Returns a vector of all the possible results of the
%    mutation of digits m1+1 through m2.
% 
% GSEGMENT
%    Segment of bits m1+1 through m2 of integer p


